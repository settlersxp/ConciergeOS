# 4. Create Users
# ------------------------------------------------------------------


def create_user(base_url: str, token: str, realm: str, username: str, password: str) -> str:
    """Create a user in a realm. Returns the user ID."""
    # Check if user exists
    resp = requests.get(
        f"{base_url}/admin/realms/{realm}/users",
        params={"username": username, "max": 1},
        headers={"Authorization": f"Bearer {token}"},
    )
    resp.raise_for_status()

    if resp.json():
        print(f"    ⏭ User {username} already exists")
        return resp.json()[0]["id"]

    resp = api_request(
        "POST",
        f"{base_url}/admin/realms/{realm}/users",
        token,
        {
            "username": username,
            "enabled": True,
            "email": f"{username}@conciergeos.local",
            "credentials": [
                {
                    "type": "password",
                    "value": password,
                    "temporary": False,
                }
            ],
            "emailVerified": True,
        },
    )
    resp.raise_for_status()

    # Extract user ID from Location header (HTTP 201 returns location)
    user_id = resp.headers.get("Location", "").split("/")[-1]
    if not user_id:
        # Fallback: query by username again
        resp = requests.get(
            f"{base_url}/admin/realms/{realm}/users",
            params={"username": username, "max": 1},
            headers={"Authorization": f"Bearer {token}"},
        )
        resp.raise_for_status()
        user_id = resp.json()[0]["id"]

    print(f"    ✓ User {username} created")
    return user_id


def create_all_users(base_url: str, token: str) -> dict[str, dict[str, str]]:
    """Create users in all realms. Returns {realm: {username: user_id}}."""
    print("[4/8] Creating users in realms...")
    user_ids: dict[str, dict[str, str]] = {}

    for realm in REALMS:
        print(f"  Realm: {realm}")
        user_ids[realm] = {}
        for username, config in USERS.items():
            user_ids[realm][username] = create_user(
                base_url, token, realm, username, config["password"]
            )
    print()
    return user_ids


# ------------------------------------------------------------------
# 5. Assign Users to Roles
# ------------------------------------------------------------------


def assign_user_to_roles(
    base_url: str,
    token: str,
    realm: str,
    user_id: str,
    role_names: list[str],
    username: str,
) -> None:
    """Assign a user to realm roles."""
    if not user_id:
        print(f"    ⚠ Skipping {username} (missing user ID)")
        return

    # Get all roles with their IDs and realm_access structure
    resp = requests.get(
        f"{base_url}/admin/realms/{realm}/roles",
        headers={"Authorization": f"Bearer {token}"},
    )
    resp.raise_for_status()
    all_roles = {r["name"]: r for r in resp.json()}

    # Check existing role mappings
    resp = requests.get(
        f"{base_url}/admin/realms/{realm}/users/{user_id}/role-mappings/realm",
        headers={"Authorization": f"Bearer {token}"},
    )
    resp.raise_for_status()
    existing_roles = {r["name"] for r in resp.json()}

    # Assign missing roles
    roles_payload = []
    for role_name in role_names:
        if role_name in existing_roles:
            print(f"    ⏭ {username} already has {role_name}")
            continue
        if role_name in all_roles:
            roles_payload.append({
                "id": all_roles[role_name]["id"],
                "name": role_name,
            })

    if roles_payload:
        resp = api_request(
            "POST",
            f"{base_url}/admin/realms/{realm}/users/{user_id}/role-mappings/realm",
            token,
            roles_payload,
        )
        if resp.status_code in (200, 204, 201):
            role_list = ", ".join(r["name"] for r in roles_payload)
            print(f"    ✓ {username} → {role_list}")
        else:
            print(f"    ⚠ Could not assign roles to {username}: {resp.status_code}")


def assign_all_users_to_roles(
    base_url: str,
    token: str,
    user_ids: dict[str, dict[str, str]],
    role_data: dict[str, dict[str, str]],
) -> None:
    """Assign all users to their respective roles in all realms."""
    print("[5/8] Assigning users to roles...")

    for realm in REALMS:
        print(f"  Realm: {realm}")
        for username, config in USERS.items():
            assign_user_to_roles(
                base_url,
                token,
                realm,
                user_ids[realm][username],
                config["roles"],
                username,
            )
    print()


