def create_client(base_url: str, token: str, realm: str) -> str:
    """Create the conciergeos client in a realm. Returns the client UUID.

    Keycloak 26 always generates a random secret. After creation, the actual
    secret is stored in _actual_client_secret and printed to stdout so the
    caller can set the OAUTH2_PROXY_CLIENT_SECRET environment variable.
    """
    global _actual_client_secret
    print(f"  Creating client: {CLIENT_ID} in {realm}...")

    # Check if client exists
    resp = requests.get(
        f"{base_url}/admin/realms/{realm}/clients",
        headers={"Authorization": f"Bearer {token}"},
    )
    resp.raise_for_status()
    clients = resp.json()
    match = [c for c in clients if c["clientId"] == CLIENT_ID]

    if match:
        client_uuid = match[0]["id"]
        print(f"    ⏭ Client {CLIENT_ID} already exists in {realm}, will update secret...")
    else:
        resp = api_request(
            "POST",
            f"{base_url}/admin/realms/{realm}/clients",
            token,
            {
                "clientId": CLIENT_ID,
                "enabled": True,
                "publicClient": False,
                "standardFlowEnabled": True,
                "implicitFlowEnabled": False,
                "directAccessGrantsEnabled": True,
                "serviceAccountsEnabled": False,
                "redirectUris": [
                    OIDC_MAIN_REDIRECT_URI,
                    DOMAIN_WILD_CARD,
                    LOCAL_REDIRECT_URI,
                ],
                "webOrigins": [
                    APP_DOMAIN,
                    LOCAL_WEB_ORIGIN,
                ],
                # postLogoutRedirectUris is NOT a top-level field in Keycloak 26's
                # ClientRepresentation - it's stored as a semicolon-separated attribute
                # (see OIDCConfigAttributes.POST_LOGOUT_REDIRECT_URIS = "post.logout.redirect.uris")
                "attributes": {
                    "pkce.code.challenge.method": "S256",
                    "post.logout.redirect.uris": "##".join([
                        POST_LOGOUT_URI,
                        DOMAIN_WILD_CARD,
                        LOCAL_REDIRECT_URI,
                    ]),
                },
            },
        )
        resp.raise_for_status()

        # Extract client UUID from Location header
        client_uuid = resp.headers.get("Location", "").split("/")[-1]
        if not client_uuid:
            # Fallback: query again
            resp = requests.get(
                f"{base_url}/admin/realms/{realm}/clients",
                headers={"Authorization": f"Bearer {token}"},
            )
            resp.raise_for_status()
            match = [c for c in resp.json() if c["clientId"] == CLIENT_ID]
            client_uuid = match[0]["id"]

    # Keycloak 26 ignores POST body and always generates a random secret.
    # POST to /client-secret regenerates the secret, then we read it back.
    resp = requests.post(
        f"{base_url}/admin/realms/{realm}/clients/{client_uuid}/client-secret",
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {token}",
        },
    )
    resp.raise_for_status()
    secret_value = resp.json().get("value", "")
    _actual_client_secret = secret_value
    print(f"    ✓ Client {CLIENT_ID} in {realm} (secret: {secret_value})")
    return client_uuid


def create_all_clients(base_url: str, token: str) -> dict[str, str]:
    """Create clients in all realms. Returns {realm: client_uuid}."""
