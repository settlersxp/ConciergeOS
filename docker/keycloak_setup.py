def main() -> None:
    parser = argparse.ArgumentParser(
        description="Keycloak Setup for ConciergeOS. "
        "Provisions realms, users, roles, and client configuration."
    )
    parser.add_argument(
        "host",
        nargs="?",
        default=os.environ.get("KEYCLOAK_HOST", "keycloak"),
        help="Keycloak host (default: from KEYCLOAK_HOST env var or 'keycloak')",
    )
    parser.add_argument(
        "port",
        nargs="?",
        type=int,
        default=int(os.environ.get("KEYCLOAK_PORT", "8080")),
        help="Keycloak port (default: from KEYCLOAK_PORT env var or 8080)",
    )
    args = parser.parse_args()

    base_url = get_base_url(args.host, args.port)
    admin_user = os.environ.get("KC_ADMIN_USER", "admin")
    admin_pass = os.environ.get("KC_ADMIN_PASS", "admin")

    print("=== Keycloak Setup for ConciergeOS ===")
    print(f"Connecting to: {base_url}")
    print()

    # 1. Authenticate
    token = authenticate(base_url, admin_user, admin_pass)

    # 2. Create realms
    create_realms(base_url, token)

    # 3. Create roles (replaces groups)
    role_data = create_all_roles(base_url, token)

    # 4. Create users
    user_ids = create_all_users(base_url, token)

    # 5. Assign users to roles (replaces group assignment)
    assign_all_users_to_roles(base_url, token, user_ids, role_data)

    # 6. Create clients
    client_uuids = create_all_clients(base_url, token)

    # 7. Configure role claim (replaces groups claim)
    configure_all_role_claims(base_url, token, client_uuids)

    # 7.5 Update oidc configs with actual secret
    actual_secret = _actual_client_secret
    print("[7.5/8] Updating oidc-main.toml with actual Keycloak secret...")
    update_oidc_configs(actual_secret)
    print()

    # 8. Summary
    print_summary(base_url, admin_user, admin_pass, actual_secret)


if __name__ == "__main__":
    main()