    """Assign all users to their respective roles in all realms."""
    print("[5/8] Assigning users to roles...")

    for realm in REALMS:
        print(f"  Realm: {realm}")
        for username, config in USERS.items():
            assign_user_to_roles(
                base_url,
                token,
                realm,
                user_ids[realm][username],
                config["roles"],
                username,
            )
    print()


